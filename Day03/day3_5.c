// arithmetic ops : + - * / %

#include <stdio.h>
void main()
{
    int num1,num2;
    printf("Enter First Value: \n");
    scanf("%d",&num1);
    printf("Enter Second Value: \n");
    scanf("%d",&num2);
    printf("Addition= %d\n",num1+num2);
    printf("Subtraction= %d\n",num1-num2);
    printf("Multiplication= %d\n",num1*num2);
    printf("Division= %d\n",num1/num2);
    printf("Division= %d\n",num1%num2);

}